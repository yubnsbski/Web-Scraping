DOC_TYPES = "120 140"  # 有価証券報告書 + 四半期報告書
MAX_PERIODS = 4

HEADER = """\
# Nikkei 225 EDINET (金融庁) targets — generated by
# scripts/build_nikkei225_edinet_registry.py (do not edit by hand).
#
# Same safety boundary as the rest of the registry: source_type=public_api,
# provider=edinet, allowed=true. Financial numbers come from official filings
# via the EDINET API (not scraped HTML). secCode is derived as ticker+"0", so
# company names are display-only and unknown/changed tickers degrade gracefully.
#
# Per-entry fields used by the EDINET connector:
#   doc_types   : EDINET docTypeCode values (120=有価証券報告書, 140=四半期報告書)
#   max_periods : recent filings kept per ticker; weekly runs accumulate history.
"""

OUTPUT_PATH = Path("examples/source_registry_nikkei225_edinet.yaml")


def _ascii_slug(ticker: str) -> str:
    return f"{ticker}_edinet"


def render_yaml() -> str:
    seen: set[str] = set()
    lines = [HEADER, "sources:"]
    for ticker, company in COMPANIES:
        if ticker in seen:
            raise ValueError(f"duplicate ticker in COMPANIES: {ticker}")
        seen.add(ticker)
        lines.extend(
            [
                f'  - name: "{_ascii_slug(ticker)}"',
                f'    ticker: "{ticker}"',
                f'    company: "{company}"',
                '    source_type: "public_api"',
                '    provider: "edinet"',
                '    method: "api"',
                "    allowed: true",
                f'    doc_types: "{DOC_TYPES}"',
                f"    max_periods: {MAX_PERIODS}",
                "",
            ]
        )
    return "\n".join(lines).rstrip() + "\n"


def main() -> None:
    OUTPUT_PATH.write_text(render_yaml(), encoding="utf-8")
    print(f"wrote {len(COMPANIES)} targets to {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
